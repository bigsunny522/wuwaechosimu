/** プレースホルダー {0}, {1}, … を args で置換 */
export function interpolate(tmpl: string, args: (string | number)[]): string {
  return args.reduce<string>(
    (s, v, i) => s.replace(`{${i}}`, String(v)),
    tmpl,
  );
}
